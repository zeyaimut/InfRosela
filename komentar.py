# progeam menghitung luas panjang degan input dari user
#meminta input panjang dan lebar dari pengguna
panjang = float(input("masukan panjang(meter): "))
lebar = float(input("masukan lebar(meter): "))
#menghitung luas
luas = panjang * lebar
#menampilkan hasl
print("luas persegi panjang adalah:", luas, "meter persegi")
