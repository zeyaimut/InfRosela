sela_a = 2
sela_b = 3
sela_op = '+'  # operator diinput oleh pengguna

if sela_op == '+':
    print("nilai = ", sela_a + sela_b)
elif sela_op == '-':
    print("nilai = ", sela_a - sela_b)
elif sela_op == '*':
    print("nilai = ", sela_a * sela_b)
elif sela_op == '/':
    print("nilai = ", sela_a / sela_b)
else:
    print("Operator tidak dikenali.")